                <h1>Endicott's Live Buoy </h1>

                <p>Endicott's water quality buoy collects and transmits to endicottlivebuoy.com on
                    weather and in-lake conditions every thirty minutes, round-the-clock, 365 days of the year </p>

                <p>The Endicott Live Buoy track's the temperature of the air and water, along with humidity,
                    pressure, wind speed and direction. The Endicott Live Buoy is located at the Endicott
                    College main campus in Beverly, Massachusetts in the Lake directly next to the Ginger Judge
                    Life Science building. </p>
